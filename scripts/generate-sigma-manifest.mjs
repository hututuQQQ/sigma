import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { assertSigmaManifest, sigmaManifest } from "./lib/sigma-manifest.mjs";

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const generatedTypeScriptPaths = ["agent-config", "agent-execution"].map((name) =>
  path.join(rootDir, "packages", name, "src", "generated-project-facts.ts"));
const generatedMarkdownPath = path.join(rootDir, "docs", "generated", "project-facts.md");

function typescriptSource(manifest) {
  return `/* Generated by scripts/generate-sigma-manifest.mjs. Do not edit. */\n`
    + `export const SIGMA_PROJECT_FACTS = ${JSON.stringify(manifest, null, 2)} as const;\n`;
}

function markdownSource(manifest) {
  return [
    "<!-- Generated by scripts/generate-sigma-manifest.mjs. Do not edit. -->",
    "## Project facts",
    "",
    `- Product: ${manifest.productVersion}`,
    `- Node: ${manifest.toolchains.node}`,
    `- pnpm: ${manifest.toolchains.pnpm}`,
    `- Rust: ${manifest.toolchains.rust}`,
    `- Rust coverage: ${manifest.toolchains.rustCoverage}`,
    `- Release targets: ${manifest.release.targets.join(", ")}`,
    `- Evaluation: ${manifest.evaluation.provider}/${manifest.evaluation.model}`,
    ""
  ].join("\n");
}

async function assertCopy(filePath, expected, label) {
  const actual = await readFile(filePath, "utf8").catch(() => "");
  if (actual !== expected) throw new Error(`${label} is stale; run pnpm generate:manifest.`);
}

async function checkMirrors(manifest) {
  const packageJson = JSON.parse(await readFile(path.join(rootDir, "package.json"), "utf8"));
  const nodeVersion = (await readFile(path.join(rootDir, ".node-version"), "utf8")).trim();
  const workflowRoot = path.join(rootDir, ".github", "workflows");
  const workflowFiles = (await readdir(workflowRoot)).filter((name) => name.endsWith(".yml"));
  const workflows = await Promise.all(workflowFiles.map(async (name) => ({
    name, source: await readFile(path.join(workflowRoot, name), "utf8")
  })));
  const expected = {
    nodeVersion: manifest.toolchains.node,
    packageVersion: manifest.productVersion,
    packageManager: `pnpm@${manifest.toolchains.pnpm}`,
    packageNode: manifest.toolchains.node
  };
  const actual = {
    nodeVersion,
    packageVersion: packageJson.version,
    packageManager: packageJson.packageManager,
    packageNode: packageJson.engines?.node
  };
  if (JSON.stringify(actual) !== JSON.stringify(expected)) {
    throw new Error(`Project metadata differs from sigma-manifest.json: ${JSON.stringify(actual)}.`);
  }
  const workflowFacts = {
    NODE_VERSION: manifest.toolchains.node,
    PNPM_VERSION: manifest.toolchains.pnpm,
    RUST_VERSION: manifest.toolchains.rust,
    RUST_COVERAGE_TOOLCHAIN: manifest.toolchains.rustCoverage
  };
  for (const workflow of workflows) {
    for (const [key, expectedValue] of Object.entries(workflowFacts)) {
      const match = workflow.source.match(new RegExp(`^\\s*${key}:\\s*([^\\s#]+)`, "mu"));
      if (match && match[1] !== expectedValue) {
        throw new Error(`${workflow.name} is stale: ${key}=${match[1]}, expected ${expectedValue}.`);
      }
    }
  }
  const ci = workflows.find((workflow) => workflow.name === "ci.yml")?.source ?? "";
  for (const [key, value] of Object.entries(workflowFacts)) {
    if (!ci.includes(`${key}: ${value}`)) throw new Error(`CI bootstrap is stale: expected '${key}: ${value}'.`);
  }
  const packageDirectories = await readdir(path.join(rootDir, "packages"), { withFileTypes: true });
  for (const directory of packageDirectories.filter((entry) => entry.isDirectory())) {
    const manifestPath = path.join(rootDir, "packages", directory.name, "package.json");
    const workspacePackage = JSON.parse(await readFile(manifestPath, "utf8"));
    if (workspacePackage.version !== manifest.productVersion) {
      throw new Error(`${directory.name} version differs from sigma-manifest.json.`);
    }
    if (workspacePackage.engines?.node !== `>=${manifest.toolchains.node}`) {
      throw new Error(`${directory.name} Node engine differs from sigma-manifest.json.`);
    }
  }
  const scripts = packageJson.scripts ?? {};
  if (!String(scripts["test:coverage:native-protocol"] ?? "").includes(`+${manifest.toolchains.rustCoverage}`)) {
    throw new Error("Native coverage toolchain differs from sigma-manifest.json.");
  }
  for (const [name, command] of Object.entries(scripts)) {
    if (!name.startsWith("bench:tb:deepseek")) continue;
    const expectedModel = `--model ${manifest.evaluation.model}`;
    if (!String(command).includes(`--provider ${manifest.evaluation.provider}`)
      || !String(command).includes(expectedModel)) {
      throw new Error(`${name} evaluation defaults differ from sigma-manifest.json.`);
    }
  }
  const documentation = await Promise.all(["README.md", "VALIDATION.md"].map((name) =>
    readFile(path.join(rootDir, name), "utf8")));
  for (const source of documentation) {
    for (const version of [manifest.toolchains.node, manifest.toolchains.pnpm]) {
      if (!source.includes(version)) throw new Error(`Documentation is missing pinned version ${version}.`);
    }
  }
}

async function main() {
  const manifest = assertSigmaManifest(sigmaManifest);
  const generatedTypeScript = typescriptSource(manifest);
  const generatedMarkdown = markdownSource(manifest);
  if (process.argv.includes("--check")) {
    for (const filePath of generatedTypeScriptPaths) {
      await assertCopy(filePath, generatedTypeScript, `Generated runtime facts (${path.basename(path.dirname(filePath))})`);
    }
    await assertCopy(generatedMarkdownPath, generatedMarkdown, "Generated documentation facts");
    await checkMirrors(manifest);
    return;
  }
  await mkdir(path.dirname(generatedMarkdownPath), { recursive: true });
  for (const filePath of generatedTypeScriptPaths) await writeFile(filePath, generatedTypeScript, "utf8");
  await writeFile(generatedMarkdownPath, generatedMarkdown, "utf8");
  await checkMirrors(manifest);
}

await main();
